def full_prompt(v: dict) -> str:
    return v["philosophy"].strip() + "\n\n" + v.get("contract", SHARED_CONTRACT).strip()


def write_md(out_dir: Path, common: str, context: str, age: str, image: Path) -> None:
    """Документируем ТОЧНЫЕ промпты и вход — чтобы данные не потерялись."""
    lines = [
        "# Prompt-лаборатория — версии отчёта для SEMESH (Лиза, order 1)",
        "",
        "Цель: подобрать голос отчёта, при котором мама скажет «вау, это ценно» —",
        "узнавая ребёнка и открывая в нём новое. Все версии используют ОДНО и то же",
        "изображение, контекст и шаблон вёрстки; различается только системный промпт.",
        "",
        "## Общий вход (одинаков для всех версий)",
        f"- Изображение: `{image.relative_to(BASE_DIR).as_posix()}`",
        f"- Ребёнок: {CHILD}",
        f"- Возраст на дату рисунка (посчитан нами): **{age}**",
        f"- Рисунок (поля родителя): {DRAWING}",
        "",
        "Собранный `common_context` (как уходит в модель):",
        "```", common, "```",
        "Собранный `context` рисунка (как уходит в модель):",
        "```", context, "```",
        "",
        "Раунды: V1–V5 — первый веер версий (контракт SHARED). "
        "V11–V13 — комбинации по рекомендациям заказчика (контракт COMBO).",
        "",
        "## Версии — ПОЛНЫЙ системный промпт каждой (источник истины)",
    ]
    for v in VARIANTS:
        contract_name = {id(COMBO_CONTRACT): "COMBO", id(COMBO2_CONTRACT): "COMBO2"}.get(
            id(v.get("contract")), "SHARED")
        lines += [
            f"### {v['title']}  (`{v['id']}_{v['label']}`)",
            f"- Линтер «внутренних состояний»: {'включён' if v['lint'] else 'выключен'} "
            f"| контракт: {contract_name}",
            f"- Идея: {v['desc']}",
            "",
            "Полный системный промпт (ровно то, что уходит в модель):",
            "```", full_prompt(v), "```",
            "",
        ]
    (out_dir / "VARIANTS_PROMPTS.md").write_text("\n".join(lines), encoding="utf-8")


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--image", type=Path,
                    default=BASE_DIR / "data" / "drawings" / "test" / "drawing_1.jpeg")
    ap.add_argument("-o", "--out", type=Path,
                    default=BASE_DIR / "data" / "drawings" / "test")
    ap.add_argument("--only", default=None,
                    help="сгенерировать только указанные id через запятую (напр. v4,v5)")
    args = ap.parse_args()

    if not args.image.exists():
        print(f"ERROR: image not found: {args.image}")
        return 1

    age = _age_display(CHILD["birth_ym"], DRAWING["drawn_at"])
    common = child_to_common(CHILD)
    context = drawing_to_story(DRAWING, age_display=age)
    out = args.out
    out.mkdir(parents=True, exist_ok=True)
    write_md(out, common, context, age, args.image)
    print(f"input: {args.image.name} | age: {age.encode('ascii', 'replace').decode()}")
    print(f"out:   {out}")

    only = set(args.only.split(",")) if args.only else None
    ok = 0
    for v in VARIANTS:
        if only and v["id"] not in only:
            continue
        tag = f"{v['id']}_{v['label']}"
        print(f"-> {tag} (lint={v['lint']}) ...")
        try:
            result = generate_report(
                [args.image], [context], common_context=common,
                system_prompt=full_prompt(v), enable_lint=v["lint"],
                raw_dump_dir=out / f"raw_{v['id']}")
        except ReportGenerationError as e:
            print(f"   FAILED: {str(e).encode('ascii', 'replace').decode()}")
            for line in e.attempts_log[-2:]:
                print("    ", line.encode("ascii", "replace").decode())
            continue

        if isinstance(result.report, InsufficientReport):
            print("   INSUFFICIENT INPUT — skipped")
            continue

        (out / f"report_{tag}.json").write_text(
            json.dumps(result.report.model_dump(), ensure_ascii=False, indent=2),
            encoding="utf-8")
        drawings = [
            {"src": "data:image/jpeg;base64," + base64.b64encode(j).decode("ascii"),
             "caption": result.report.child.name}
            for j in result.image_jpegs
        ]
        html_path, pdf_path = render_report_files(
            result.report, drawings, generated_date=ru_date(datetime.date.today()),
            out_dir=out, basename=f"report_{tag}")
        print(f"   OK: {pdf_path.name} ({pdf_path.stat().st_size // 1024} KB, "
              f"attempts={result.attempts_used})")
        ok += 1

    print(f"DONE: {ok} report(s) -> {out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
