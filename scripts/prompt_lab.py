def _age_display(birth_ym: str, drawn_at: str) -> str:
    """Возраст на дату рисунка (копия логики app.jobs — без импорта веб-стека)."""
    by, bm = map(int, birth_ym.split("-"))
    dy, dm = map(int, drawn_at.split("-"))
    total = (dy * 12 + dm) - (by * 12 + bm)
    years, months = divmod(max(total, 0), 12)

    def plural(n, one, few, many):
        if n % 10 == 1 and n % 100 != 11:
            return one
        if 2 <= n % 10 <= 4 and not 12 <= n % 100 <= 14:
            return few
        return many

    parts = []
    if years:
        parts.append(f"{years} {plural(years, 'год', 'года', 'лет')}")
    if months:
        parts.append(f"{months} {plural(months, 'месяц', 'месяца', 'месяцев')}")
    return " ".join(parts) or "меньше месяца"


