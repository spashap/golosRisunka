"""Линтер отчёта: программный бэкстоп правил подачи (философия 2.3, PROMPT_VERSION 4.0).

Промпт сам по себе не даёт 100% — сэмплинг при temp 0.5 дрейфует. После валидации
JSON отчёт прогоняется через линтер; найденные нарушения чинятся повторным текстовым
вызовом Gemini (repair pass в gemini.py).

ВАЖНО (разворот 2.3): линтер БОЛЬШЕ НЕ баним слова о состояниях/эмоциях/характере —
это теперь legitimate глубина продукта (зона 2/3). Вместо этого он проверяет НАЛИЧИЕ
БЕЗОПАСНОЙ ОПРАВЫ вокруг такой интерпретации и ловит только то, что запрещено всегда.

Два класса правил:
  A. HARD_BANNED — нарушение ВСЕГДА, оправа не спасает (голый диагноз-как-факт,
     командный тон, катастрофизация, предсказание-таланта-факт, бренд-баны).
  B. REQUIRES_FRAME — сенситивные термины (тревож/характер/настроение…) НЕ нарушение
     сами по себе; нарушение — если они стоят БЕЗ безопасной оправы рядом:
       - «тяжёлые» клинические термины требуют И гипотезного оборота, И атрибуции;
       - «лёгкие» черты/состояния требуют гипотезного оборота ЛИБО речь идёт о самой
         работе (характер ЛИНИИ, настроение РИСУНКА — это про артефакт, не про ребёнка).

Условия 3 (видимая деталь) и 4 (возврат к ребёнку) надёжно регэкспом не ловятся —
их несёт промпт; линтер проверяет детектируемые 1–2 (гипотеза + атрибуция).
Репэйр (gemini.py) переписан под «ДОБАВЬ оправу / смягчи», а не «удали».
"""
from __future__ import annotations

import re

# --- A. ВСЕГДА запрещено (оправа не помогает) -----------------------------------
HARD_BANNED: list[tuple[str, str]] = [
    # дословные бренд-баны
    (r"эмоциональн\w*\s+интеллект\w*", "запрещено «эмоциональный интеллект»"),
    (r"хорош\w+\s+вкус\w*", "запрещено «хороший вкус»"),
    (r"баз\w+\s+для\s+красив\w+\s+почерк\w*", "запрещено «база для красивого почерка»"),
    (r"лайнер\w*", "пиши «линер», не «лайнер»"),
    # командный тон
    (r"\b(?:купите|подарите|приобретите|купи|подари)\b", "командный тон — пиши «можно предложить»"),
    (r"\bобязательно\b", "«обязательно» — командный/давящий тон, смягчи"),
    # предсказание судьбы/таланта как факт
    (r"(?:станет|будет)\s+(?:художник\w+|артист\w+|дизайнер\w+)", "прогноз будущего таланта запрещён"),
    # голый диагноз / состояние-как-факт (диагностические конструкции, не гипотезы)
    (r"у\s+(?:реб[её]нк\w+|него|не[ёе]|маленьк\w+\s+\w+|[А-ЯЁ][а-яё]+)\s+\w*\s*"
     r"(?:тревожност\w+|депресси\w+|невроз\w+|низк\w+\s+самооценк\w+|высок\w+\s+самооценк\w+)",
     "голый диагноз/состояние-как-факт — недопустимо даже в оправе"),
    (r"(?:рисунок|это|работа)\s+показывает,?\s+что\s+реб[её]нок\s+"
     r"(?:несчастен|подавлен|страдает|тревожен|агрессив\w+|одинок)",
     "состояние-как-факт — недопустимо; только гипотеза в оправе"),
    (r"это\s+означает,?\s+что\s+реб[её]нок", "«это означает, что ребёнок…» — вывод-как-факт запрещён"),
    # катастрофизация / пугающие трактовки
    (r"серь[её]зн\w+\s+проблем\w+", "катастрофизация запрещена"),
    (r"срочно\s+к\s+(?:врач\w+|специалист\w+|психолог\w+)", "катастрофизация/паника запрещена"),
    (r"скрыт\w+\s+травм\w+", "пугающая трактовка «скрытая травма» запрещена"),
]

# --- B. Сенситивные термины: нарушение только БЕЗ оправы -------------------------
# «тяжёлые» клинические: нужны И гипотеза, И атрибуция. \b спереди, чтобы не ловить
# безобидные «вс-тревож-ит / о-тревож-ить» (тревога РОДИТЕЛЯ, не диагноз ребёнка).
HEAVY_TERMS = re.compile(
    r"\bтревож\w*|\bдепресс\w*|\bневроз\w*|\bтравм\w*|\bагресс\w*|\bстрах\w*|"
    r"\bфоби\w*|\bсуицид\w*", re.IGNORECASE)
# «лёгкие» черты/состояния: нужен гипотезный оборот ЛИБО речь идёт о самой работе
LIGHT_TERMS = re.compile(
    r"самооценк\w*|темперамент\w*|характер(?!н)\w*|настроени\w*|замкнут\w*|"
    r"стеснительн\w*|неувер\w*|одиночеств\w*|печал\w*|груст\w*|\bзлост\w*|\bгнев\w*|"
    r"внутренн\w+\s+(?:мир\w*|сил\w*|состояни\w*)", re.IGNORECASE)

# гипотезный оборот (условие 2 оправы)
HEDGE = re.compile(
    r"может\s+(?:говорить|отражать|быть|означать|свидетельств\w+)|"
    r"иногда\s+связыва\w+|часто\s+связыва\w+|можно\s+прочита\w+|можно\s+прочесть|"
    r"похож\w+\s+на|созда[её]т\s+впечатлени\w+|в\s+этом\s+ключе|это\s+гипотез\w+|"
    r"догадк\w+|не\s+вывод|можно\s+(?:увидеть|услышать)\s+как|словно|как\s+будто", re.IGNORECASE)
# атрибуция к традиции/автору (условие 1 оправы)
ATTRIBUTION = re.compile(
    r"проективн\w+|традици\w+|подход\w+|Маховер|Ловенфельд|Lowenfeld|Выготск\w+|"
    r"Пиаже|Piaget|Kellogg|Гудинаф|Goodnow|Burkitt|согласно\b|по\s+наблюдени\w+\s+\w+", re.IGNORECASE)
# термин относится к самому артефакту (характер ЛИНИИ, настроение РИСУНКА), не к ребёнку:
# ищем артефакт-существительное СРАЗУ после термина (узкое окно), а не во всём контексте —
# иначе «рисунок» где угодно рядом ложно оправдал бы «характер Лизы».
WORK_REF = re.compile(
    r"работ\w*|рисун\w*|картин\w*|сцен\w*|сюжет\w*|композ\w*|лини\w*|штрих\w*|"
    r"мазок|мазк\w*|цвет\w*|пейзаж\w*|персонаж\w*|изображени\w*", re.IGNORECASE)
_WORK_LOOKAHEAD = 40  # символов после термина, где артефакт-существительное снимает претензию

# глобальные контексты, в которых совпадение НЕ считается нарушением
ALLOWED_CONTEXTS = [
    "не является", "не ставим диагноз", "не диагноз", "без диагноз",
    "не оценива", "образовательное наблюдение", "не медицинск", "не психологическ",
]

_WINDOW = 200  # символов в каждую сторону вокруг термина — окно проверки оправы

# поля верхнего уровня, которые проверяем
_CHECK_FIELDS = ("context_summary", "introduction", "about_child", "conclusion")


def _hard_scan(text: str, where: str) -> list[dict]:
    hits = []
    for pattern, why in HARD_BANNED:
        for m in re.finditer(pattern, text, re.IGNORECASE):
            s, e = max(0, m.start() - 80), min(len(text), m.end() + 80)
            ctx = text[s:e]
            if any(a in ctx.lower() for a in ALLOWED_CONTEXTS):
                continue
            hits.append({"where": where, "match": m.group(0), "why": why, "context": ctx})
    return hits


def _frame_scan(text: str, where: str) -> list[dict]:
    """Сенситивные термины без безопасной оправы рядом."""
    hits = []
    for term_re, heavy in ((HEAVY_TERMS, True), (LIGHT_TERMS, False)):
        for m in re.finditer(term_re, text):
            s, e = max(0, m.start() - _WINDOW), min(len(text), m.end() + _WINDOW)
            window = text[s:e]
            wl = window.lower()
            if any(a in wl for a in ALLOWED_CONTEXTS):
                continue
            has_hedge = bool(HEDGE.search(window))
            has_attr = bool(ATTRIBUTION.search(window))
            if heavy:
                if has_hedge and has_attr:
                    continue
                why = ("интерпретация тяжёлого состояния без полной оправы — нужны "
                       "атрибуция к традиции/автору И гипотезный оборот (см. раздел 3); "
                       "ДОБАВЬ оправу или смягчи, НЕ удаляй смысл")
            else:
                # лёгкая черта: достаточно гипотезы; либо термин относится к самому
                # артефакту (артефакт-существительное сразу после термина, узкое окно)
                after = text[m.end():m.end() + _WORK_LOOKAHEAD]
                if has_hedge or WORK_REF.search(after):
                    continue
                why = ("интерпретация черты/состояния ребёнка без гипотезного оборота — "
                       "оформи как гипотезу («может говорить о…») с возвратом к ребёнку "
                       "(см. раздел 3); НЕ удаляй смысл")
            ctx = text[max(0, m.start() - 80):min(len(text), m.end() + 80)]
            hits.append({"where": where, "match": m.group(0), "why": why, "context": ctx})
    return hits


def _scan(text: str, where: str) -> list[dict]:
    return _hard_scan(text, where) + _frame_scan(text, where)


def find_violations(report_data: dict) -> list[dict]:
    """report_data — dict валидированного отчёта (model_dump)."""
    hits: list[dict] = []
    for f in _CHECK_FIELDS:
        if report_data.get(f):
            hits.extend(_scan(str(report_data[f]), f))
    for i, d in enumerate(report_data.get("dimensions") or []):
        for f in ("observation", "research_note"):
            if d.get(f):
                hits.extend(_scan(str(d[f]), f"dimensions[{i}].{f} ({d.get('title')})"))
    for field in ("understanding_recommendations", "art_recommendations"):
        for i, r in enumerate(report_data.get(field) or []):
            hits.extend(_scan(str(r), f"{field}[{i}]"))
    for i, sp in enumerate(report_data.get("specialists") or []):
        hits.extend(_scan(str(sp.get("reason", "")), f"specialists[{i}].reason"))
    for i, dd in enumerate(report_data.get("development_directions") or []):
        hits.extend(_scan(str(dd.get("text", "")), f"development_directions[{i}]"))
    return hits
